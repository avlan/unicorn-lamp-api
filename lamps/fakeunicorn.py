class unicornhat:
    """
    a copy of unicornhat that works without the class
    but doesn't actually do anything
    """

    @staticmethod
    def brightness(level):
        """
        adjust the brightness of the fakelamp
        """

    @staticmethod
    def set_all(red, green, blue):
        """
        change the colors of the fakelamp
        """

    @staticmethod
    def show():
        """
        show the current colors of the fakelamp
        """

    @staticmethod
    def clear():
        """
        clear the current colors of the fakelamp
        """

    @staticmethod
    def get_pixel(x, y):
        """
        get pixel value x, y of the fakelamp
        """
